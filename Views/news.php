	<script>
    $(document).ready(function () {
        // Submit the search form
        $("#news-search-form").submit(function (event) {
            event.preventDefault();
            filterNewsByTitle($("#news-search-input").val());
        });

        // Function to filter news by title
        function filterNewsByTitle(title) {
            // Hide all news items
            $(".contain").hide();

            // Show only the news items that match the search title
            $(".contain:contains('" + title + "')").show();
        }

        // Custom jQuery expression for case-insensitive :contains
        $.expr[":"].contains = $.expr.createPseudo(function (arg) {
            return function (elem) {
                return $(elem).text().toUpperCase().indexOf(arg.toUpperCase()) >= 0;
            };
        });
    });
</script>

